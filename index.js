// ГЛАВНЫЙ ОБРАБОТЧИК ДЛЯ ВСЕХ СООБЩЕНИЙ ЧАТА
bot.on('message', async (ctx) => {
  const text = ctx.message.text;
  if (!text) return;

  const chatId = ctx.message.chat.id;
  const username = ctx.message.from.username || ctx.message.from.first_name || 'Аноним';
  const kvKey = `chat_history:${chatId}`;

  // 1. Пассивное логирование (сохраняем обычные сообщения чата без префикса !sb)
  if (!text.startsWith('!sb')) {
    try {
      await kv.rpush(kvKey, `[${username}]: ${text}`);
      await kv.ltrim(kvKey, -150, -1); // Держим историю строго в лимите последние 150 строк
    } catch (err) {
      console.error('Ошибка записи в KV:', err);
    }
    return;
  }

  // Защита от дублирования вызова help
  if (text.trim() === '!sb help') return;

  // 2. Обработка ИИ-команд (!sb summary, !sb rating и т.д.)
  try {
    await ctx.sendChatAction('typing');

    // Тянем накопленную историю чата из облачной базы Redis
    const historyArray = await kv.lrange(kvKey, 0, -1);
    const logContext = historyArray.length > 0 ? historyArray.join('\n') : 'Чат пуст. Переписки нет.';

    const prompt = `КОНТЕКСТ ЧАТА:\n${logContext}\n\nКОМАНДА ОТ ПОЛЬЗОВАТЕЛЯ: ${text}\n\nВыполни команду строго по инструкции Стат Боя.`;

    const result = await model.generateContent({
      contents: [{ role: 'user', parts: [{ text: prompt }] }],
      systemInstruction: SYSTEM_PROMPT,
    });

    // Отправляем ответ, защищенный режимом HTML
    await ctx.reply(result.response.text(), { 
      reply_to_message_id: ctx.message.message_id,
      parse_mode: 'HTML'
    });
  } catch (e) {
    console.error('Ошибка Gemini ИИ или Telegram API:', e);
    await ctx.reply('Бот сломался от твоего кринжа. Попробуй позже.');
  }
});

// НАДЁЖНЫЙ И ЦЕЛЬНЫЙ ОБРАБОТЧИК ВЕБХУКА ДЛЯ SERVERLESS VERCEL
module.exports = async (req, res) => {
  try {
    if (req.method === 'POST') {
      const update = typeof req.body === 'string' ? JSON.parse(req.body) : req.body;
      await bot.handleUpdate(update, res);
    } else {
      res.status(200).send('<h1>StatBoy готов к работе через Вебхук и Vercel KV!</h1>');
    }
  } catch (err) {
    console.error('Критическая ошибка вебхука:', err);
    res.status(200).send('Error handled');
  }
};
