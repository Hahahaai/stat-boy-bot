let chatHistory = [];

bot.on('message', async (ctx) => {
  const text = ctx.message.text;
  const username = ctx.message.from.username || ctx.message.from.first_name;

  if (!text) return;

  if (!text.startsWith('!sb')) {
    chatHistory.push(`[${username}]: ${text}`);
    if (chatHistory.length > 300) chatHistory.shift();
    return;
  }

  try {
    ctx.sendChatAction('typing');
    const logContext = chatHistory.join('\n');
    
    const prompt = `КОНТЕКСТ ЧАТА:\n${logContext}\n\nКОМАНДА ОТ ПОЛЬЗОВАТЕЛЯ: ${text}\n\nВыполни команду строго по инструкции Стат Боя.`;
    
    const result = await model.generateContent({
      contents: [{ role: 'user', parts: [{ text: prompt }] }],
      systemInstruction: SYSTEM_PROMPT,
    });

    ctx.reply(result.response.text(), { reply_to_message_id: ctx.message.message_id });
  } catch (e) {
    ctx.reply('Бот сломался от твоего кринжа. Попробуй позже.');
  }
});

bot.launch();
